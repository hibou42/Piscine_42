#include<unistd.h>

void	ft_print_tab(char **tab)
{
	int	x;
	int	y;

	x = 1;
	y = 1;
	while (x <= 4)
	{
		while (y <= 4)
		{
			write(1, &tab[x][y], 1);
			y++;
			if (y < 6)
				write(1, " ", 1);
		}
		x++;
		y = 1;
		write(1, "\n", 1);
	}
}
