#include <unistd.h>

void	ft_print_comb2(void)
{
	int	a;
	int	b;
	int	x;
	int	y;

	a = '0';
	b = '0';
	x = '0';
	y = '1';
	write(1, &a, 1);
	write(1, &b, 1);
	write(1, " ", 2);
	write(1, &x, 1);
	write(1, &y, 1);
	write(1, ", ", 2);
}
